int wxWidgets::DefineSearch(std::string str, bool *found)
{
	if (str.find("wx") == 0 && str.length() > 0)
		str = str.substr(2);
	std::map<std::string, int>::iterator it;
	it = defineMap.find(str);
	if (it != defineMap.end()) {
		if (found != NULL)
			*found = true;
		return it->second;
	} else {
		if (found != NULL)
			*found = false;
		return -1;
	}
}
